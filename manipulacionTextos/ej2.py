"""Concatenar caracteres o cadenas utilizando el operador + para formar nuevas cadenas."""

palabra1 = "Hola, "
palabra2 = "Mundo"
caracter1 = "!"

#Concatenar dos cadenas:
cadenaConcatenada = palabra1 + palabra2
print(f"Resultado: {cadenaConcatenada}")

#Concatenar cadena y caracter:
cadenaCaracter = cadenaConcatenada + caracter1
print(f"Resultado final: {cadenaCaracter}")